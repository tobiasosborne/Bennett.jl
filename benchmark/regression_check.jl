#!/usr/bin/env julia
# ---- Compile-time + gate-count regression check ----
#
# Run as:  julia --project=. benchmark/regression_check.jl [--update]
#
# Compiles a small canonical corpus, records (gate_count, Toffoli, wires,
# compile_seconds), and compares against baselines in
# `benchmark/regression_baselines.jsonl`. Exits non-zero on regression.
#
# `--update` rewrites the baselines file with current measurements
# (use after an INTENTIONAL change that updates gate counts; never use
# blindly to silence a regression).
#
# Filed as Bennett-g7r8 / U107 after Bennett-qxg9 (where a sizehint!
# bug went undetected for ~10 days because no compile-time canary
# existed). Designed to catch the qxg9 regression class:
#
#   - O(N²) compile-time blowup at scaled workloads (cf at small max_n
#     is the canary — qxg9 was 31s → 1213s at max_n=64; we run max_n=16
#     here as a fast proxy that rises ~30× under the same bug).
#   - Silent gate-count drift on canonical functions (x+1, soft_fma,
#     md5_step_F_s7, x²+3x+1).
#
# NOT wired into CI per CLAUDE.md §14 — invoke locally before merging
# anything that touches src/lower.jl, src/ir_extract.jl, or peephole /
# fold paths.

using Bennett
using Bennett: gate_count, t_count, ancilla_count, verify_reversibility
using Dates
using Printf

const BASELINES_PATH = joinpath(@__DIR__, "regression_baselines.jsonl")
const UPDATE_MODE = "--update" in ARGS

# Compile-time tolerance: warn at WARN_X, fail at FAIL_X. Compile-time
# has ~10% run-to-run variance from precompile cache state and GC, so
# small deltas are noise. The qxg9 regression was 33×, well above 5×.
const WARN_X = 2.0
const FAIL_X = 5.0

# ---- Corpus definition ----
#
# Each entry: (label, builder_thunk). Keep total runtime < 60s for
# fast feedback. Heavy entries (cf, soft_fma) cap at ~5s each.

include(joinpath(@__DIR__, "sweep_persistent_impls_gen.jl"))

const CORPUS = [
    # Integer arithmetic baselines (CLAUDE.md §6: pinned regressions)
    ("x+1_i8",   () -> reversible_compile(x -> x + Int8(1),  Int8)),
    ("x+1_i16",  () -> reversible_compile(x -> x + Int16(1), Int16)),
    ("x+1_i32",  () -> reversible_compile(x -> x + Int32(1), Int32)),
    ("x+1_i64",  () -> reversible_compile(x -> x + Int64(1), Int64)),
    ("xsq3x1_i8", () -> reversible_compile(x -> x*x + Int8(3)*x + Int8(1), Int8)),

    # MD5 step canary
    ("md5_step_F_s7", () -> begin
        md5_F(x::UInt32, y::UInt32, z::UInt32) = (x & y) | (~x & z)
        rotl32(x::UInt32, n::Int) = (x << n) | (x >> (32 - n))
        function step_F(a::UInt32, b::UInt32, c::UInt32, d::UInt32, k::UInt32, w::UInt32)
            b + rotl32(md5_F(b, c, d) + a + k + w, 7)
        end
        reversible_compile(step_F, UInt32, UInt32, UInt32, UInt32, UInt32, UInt32)
    end),

    # Soft-float canaries (transcendental class is in test_softfexp_*.jl)
    ("soft_fadd", () -> reversible_compile(Bennett.soft_fadd, UInt64, UInt64)),
    ("soft_fma",  () -> reversible_compile(Bennett.soft_fma,  UInt64, UInt64, UInt64)),

    # Persistent-DS qxg9 canary: cf at moderate max_n
    ("cf_max_n16", () -> reversible_compile(cf_demo_16, Int8, Int8; optimize=false)),
]

# ---- Measurement ----

function measure(label::String, builder::Function)
    GC.gc()  # baseline GC state for stable timing
    t0 = time()
    c = builder()
    elapsed = time() - t0
    gc = gate_count(c)
    return (
        label = label,
        gates_total = gc.total,
        Toffoli = gc.Toffoli,
        wires = c.n_wires,
        compile_seconds = round(elapsed, digits=3),
    )
end

function load_baselines(path::String)
    isfile(path) || return Dict{String,NamedTuple}()
    out = Dict{String,NamedTuple}()
    for line in eachline(path)
        isempty(strip(line)) && continue
        # Hand-parse: expects label/gates_total/Toffoli/wires/compile_seconds keys.
        m = match(r"\"label\"\s*:\s*\"([^\"]+)\"", line)
        m === nothing && continue
        label = m.captures[1]
        gt   = parse(Int,     match(r"\"gates_total\"\s*:\s*(-?\d+)", line).captures[1])
        tof  = parse(Int,     match(r"\"Toffoli\"\s*:\s*(-?\d+)",     line).captures[1])
        wr   = parse(Int,     match(r"\"wires\"\s*:\s*(-?\d+)",       line).captures[1])
        cs   = parse(Float64, match(r"\"compile_seconds\"\s*:\s*([0-9.eE+-]+)", line).captures[1])
        out[label] = (gates_total=gt, Toffoli=tof, wires=wr, compile_seconds=cs)
    end
    return out
end

function write_baselines(path::String, results::Vector)
    open(path, "w") do io
        println(io, "# Auto-generated by benchmark/regression_check.jl --update")
        println(io, "# Bennett-g7r8 / U107. Gate counts are CLAUDE.md §6 regression")
        println(io, "# baselines (must match exactly). Compile-seconds are perf canaries")
        println(io, "# (warn at $(WARN_X)×, fail at $(FAIL_X)×). Updated $(now()).")
        for r in results
            line = """{"label":"$(r.label)","gates_total":$(r.gates_total),"Toffoli":$(r.Toffoli),"wires":$(r.wires),"compile_seconds":$(r.compile_seconds)}"""
            println(io, line)
        end
    end
end

# ---- Main ----

println("Bennett.jl regression check (Bennett-g7r8 / U107)")
println("=" ^ 60)

baselines = load_baselines(BASELINES_PATH)
if isempty(baselines) && !UPDATE_MODE
    println("No baselines at $(BASELINES_PATH).")
    println("Run: julia --project=. benchmark/regression_check.jl --update")
    exit(2)
end

results = []
global fail_count = 0
global warn_count = 0

@printf("%-20s %12s %10s %10s %10s\n", "label", "gates", "Toffoli", "wires", "compile_s")
println("-" ^ 65)

for (label, builder) in CORPUS
    r = measure(label, builder)
    push!(results, r)
    @printf("%-20s %12d %10d %10d %10.3f", r.label, r.gates_total, r.Toffoli, r.wires, r.compile_seconds)

    if UPDATE_MODE
        println(" [updated]")
        continue
    end

    base = get(baselines, label, nothing)
    if base === nothing
        println(" [NEW — no baseline]")
        continue
    end

    # Gate count: exact match required
    gate_drift = (r.gates_total != base.gates_total) ||
                 (r.Toffoli     != base.Toffoli)     ||
                 (r.wires       != base.wires)
    # Compile time: ratio
    ratio = r.compile_seconds / max(base.compile_seconds, 0.1)

    flags = String[]
    if gate_drift
        push!(flags, @sprintf("GATES Δ (was %d/%d/%d)", base.gates_total, base.Toffoli, base.wires))
        global fail_count += 1
    end
    if ratio >= FAIL_X
        push!(flags, @sprintf("COMPILE %.1fx (was %.2fs)", ratio, base.compile_seconds))
        global fail_count += 1
    elseif ratio >= WARN_X
        push!(flags, @sprintf("compile %.1fx (was %.2fs)", ratio, base.compile_seconds))
        global warn_count += 1
    end
    println(isempty(flags) ? " ✓" : " ✗ " * join(flags, "; "))
end

println()
if UPDATE_MODE
    write_baselines(BASELINES_PATH, results)
    println("Baselines written to $(BASELINES_PATH) (n=$(length(results))).")
    exit(0)
end

println("$(length(results)) checks; $(fail_count) failures; $(warn_count) warnings")
exit(fail_count > 0 ? 1 : 0)
